#!/usr/bin/env python3
"""
A type-annotated function concat that takes a string str1 and a string str2
"""


def concat(str1: str, str2: str) -> str:
    """
    Concatenate 2 strings

    Parameters:
        str1: string
        str2: string

    Returns:
        str: string
    """
    strt = str1 + str2
    return strt
